    private void validateLogin(String username, String password) throws SQLException{
        Connection con = DriverManager.getConnection(DB_URL,DB_USER,DB_PASSWORD);
        String sql = "SELECT * FROM admins WHERE Username = ? AND Password =?";
        PreparedStatement ps = con.prepareStatement(sql);
        ps.setString(1, username);
        ps.setString(2, password);
        ResultSet rs = ps.executeQuery();
        if (rs.next()){
            JOptionPane.showMessageDialog(this,"Login Success For Administrator " + username+".");
             new Menu_frame().setVisible(true);
             this.dispose();
        }
        else{
            JOptionPane.showMessageDialog(this,"Invalid Username or Password.");
            //Signup_ButActionPerformed()
        }
    }
    
    
