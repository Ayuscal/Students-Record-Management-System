    private void showPasswordCheckboxItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_showPasswordCheckboxItemStateChanged
        // TODO add your handling code here:
         if  (showPasswordCheckbox.getState())
        {
            PassText.setEchoChar((char)0);
        }
        else if (new String(PassText.getPassword()).isEmpty()) 
        {
            PassText.setText("Enter Password");
            PassText.setForeground(Color.gray);  // Optional: Set placeholder text color
            PassText.setEchoChar((char) 0);  // Disable masking
        }   
        else {
        // If the password field is not empty, mask the password with '*'
            PassText.setEchoChar('*');  // Enable masking
    }
    }//GEN-LAST:event_showPasswordCheckboxItemStateChanged

